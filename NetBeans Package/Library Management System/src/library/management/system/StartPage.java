    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        // TODO add your handling code here:
        if(passwordField.getText().trim().length() == 0 || collegeIDField.getText().trim().length() == 0 )
        {
            JOptionPane.showMessageDialog(this, "Input Fields Empty bro-_-");
            return;
        }
        try
        {
            //Class.forName("com.mysql.jdbc.Driver").newInstance();
            Connection conn;
            conn = DriverManager.getConnection("jdbc:mysql://localhost/library","nigger", "nigger");
            System.out.println("Database is connected !");
                       
            MessageDigest md;
            String outEncoded = "";
            try 
            {
                md = MessageDigest.getInstance("SHA-256");
                md.update(passwordField.getText().getBytes());
                byte[] aMessageDigest = md.digest();

                outEncoded = Base64.getEncoder().encodeToString( aMessageDigest );
            } 
            catch (NoSuchAlgorithmException ex) 
            {
                Logger.getLogger(SignupPage.class.getName()).log(Level.SEVERE, null, ex);
            }            
            // the mysql insert statement
            String query = "select `password` from `login` where `collegeid`='"+collegeIDField.getText().trim().toString()+"'";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(query);            
            while (rs.next())
            {
                String id = rs.getString("password");
                MessageDigest mdd;
                String outencoded = "";
                try 
                {
                    mdd = MessageDigest.getInstance("SHA-256");
                    mdd.update(passwordField.getText().getBytes());
                    byte[] aMessageDigest = mdd.digest();                    
                    outencoded = Base64.getEncoder().encodeToString( aMessageDigest );
                    if(id.equals(outencoded))
                    {
                        System.out.println("Password matched!");
                        conn.close();
                        JOptionPane.showMessageDialog(this, "Log In Successful!");
                        Home page;
                        final String collid = collegeIDField.getText().trim();
                        page = new Home();
                        page.authid = collid;
                        System.out.println("Checking auth before calling: " + page.authid);
                        page.setVisible(true);
                        this.dispose();
                    }                    
                } 
                catch (Exception ex) 
                {
                    Logger.getLogger(SignupPage.class.getName()).log(Level.SEVERE, null, ex);
                }               
            }
            JOptionPane.showMessageDialog(this, "ID or Password incorrect!");
        }
        catch (SQLException ex) 
        {
            // handle any errors
            System.out.println("SQLException: " + ex.getMessage());
            //JOptionPane.showMessageDialog(this, ex.getMessage());
            System.out.println("SQLState: " + ex.getSQLState());
            System.out.println("VendorError: " + ex.getErrorCode());
        }
    }//GEN-LAST:event_loginButtonActionPerformed

